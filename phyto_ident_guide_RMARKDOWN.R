#### brightwellii
Cells are usually solitary, sometimes in short chains. They are rectangular in girdle view and roughly triangular in valve view. A large spine extends from the centre of each valve, and is surrounded by a marginal ridge. Chains are connected by the large spines. Chloroplasts are small and numerous; nucleus is central. Cells are yellow-brown in colour. 
