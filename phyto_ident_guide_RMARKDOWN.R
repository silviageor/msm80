### Melosira
#### moniliformis
Cells are pill-shaped, from roughly cylindrical to almost spherical, and are usually connected in straight chains by mucous pads. Valves have thick walls, and the valve face is flat with small, poorly developed spines. Intercalary bands are usually absent between valves, but are often seen connecting the margins of dividing cells. Chloroplasts are numerous and discoid, located along the cell wall; the nucleus is central. Cells are yellow-brown in colour.
