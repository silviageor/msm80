Cells are connected in long, twisted chains that are straight or slightly curved. Cells are longer than wide, with rounded corners. Internal spines are thin and covered with numerous small spines, arising from just within the margins and extending out roughly perpendicularly to the chain. One chloroplast per cell (Cupp 1943). Cells are yellow-brown in colour. 

