#### convolutus 
Cells are bullet-shaped and usually in straight or slightly bent chains, connected by direct contact between adjacent spines. The two valves have different shapes: The top valve is rounded and has spines arising from its centre, while the bottom valve is flat and has spines arising from just inside the margin. The spines are long, thick, and hollow. They are a constant width throughout and are covered in numerous tiny "spinelets". The large spines initially curve strongly towards the bottom, and then curve slightly away. Apertures between adjacent cells are partly or completely covered by the spines. The girdle is roughly â…“ of the cell length, and there is a distinct notch between each valve and the girdle (Cupp 1943). Chloroplasts are numerous, present inside the cell as well as inside the spines (Haigh 2010). Cells are yellow-brown in colour. 
