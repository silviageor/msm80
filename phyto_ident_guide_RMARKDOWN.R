#### delicatula
Cells are cylindrical, with flat valves, rounded only slightly on the margins. Cells are in close, straight chains. The valves have a short, slightly slanted, marginal spine that fits into a dent on the adjacent cell. Cells have large, irregularly lobed and strongly pigmented chloroplasts. Each chloroplast has a pyrenoid. Cells are yellow-brown in colour. 
